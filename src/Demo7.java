void main(){
    // hexadecimal floating point literals
    System.out.println(0xA.p2);     // A. x 2^2
    System.out.println(0xA.P10);    // A. x 2^10
    System.out.println(0x.Ap2);     // .A x 2^2
    System.out.println(0x12p2f);
    System.out.println(0x120p-2D);
    System.out.println(0x12.22p-2D);
}